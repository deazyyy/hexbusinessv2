  function bytes32ToNumString(bytes32str) {
	bytes32str = bytes32str.replace(/^0x/, '');
	var bn = new web3.utils.BN(bytes32str, 16).fromTwos(256);
	return bn.toString();
  }
  
  function bytes32ToInt(bytes32str) {
	bytes32str = bytes32str.replace(/^0x/, '');
	var bn = new web3.utils.BN(bytes32str, 16).fromTwos(256);
	return bn;
  }
  
  function padToBytes32(n) {
	while (n.length < 64) {
	  n = "0" + n;
	}
	return "0x" + n;
  }
  
